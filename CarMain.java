class Car {
    int modelNo;
    String carColor;
    String carType;
}

public class CarMain {
    public static void main(String[] args) {
        Car c1 = new Car();
        c1.modelNo = 1234;
        c1.carColor = "Red";
        c1.carType = "SUV";

        System.out.println(c1.modelNo);
        System.out.println(c1.carColor);
        System.out.println(c1.carType);
    }
}
